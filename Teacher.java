package Teacher.Bean;

public class Teacher {
	private String Name;
	private String Id;
	private String Dob;
	private String Email;
	private String Mobile_no;
	private String Designation;
	private String Sub_Faculty;
	private String Class_Assigned;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getDob() {
		return Dob;
	}
	public void setDob(String dob) {
		Dob = dob;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public String getMobile_no() {
		return Mobile_no;
	}
	public void setMobile_no(String mobile_no) {
		Mobile_no = mobile_no;
	}
	public String getDesignation() {
		return Designation;
	}
	public void setDesignation(String designation) {
		Designation = designation;
	}
	public String getSub_Faculty() {
		return Sub_Faculty;
	}
	public void setSub_Faculty(String sub_Faculty) {
		Sub_Faculty = sub_Faculty;
	}
	public String getClass_Assigned() {
		return Class_Assigned;
	}
	public void setClass_Assigned(String class_Assigned) {
		Class_Assigned = class_Assigned;
	}
	

}
